numero_1 = int(input("Escriba un número: "))
numero_2 = int(input("Escriba otro número: "))
resto_numero_1 = numero_1 % 2
resto_numero_2 = numero_2 % 2
if resto_numero_1 == resto_numero_1 and resto_numero_1 == 0:
    suma = numero_1 + numero_2
    print(f"la suma de los 2 numeros da como resultado {suma}")
else:
    print("Un número o los dos no son pares no se pueden sumar")

